# 字面量的写法
print(100)
print(3.14)
print(True)
print(False)
print("Hello World")
print(None)
print("------------------")

# 运算
print(True - 1)
print(False - 1)

# 变量 --> 变量可以存储不同类型的值（字面量），但在项目开发中，建议一个变量只存储一个类型的值
num = 666 #Interge整数类型
print(num)

num = "hello world" #String字符串类型
print(num)

num = True #Bool类型
print(num)
num = False
print(num)

num = None #NoneType空型（空类型）
print(num)
